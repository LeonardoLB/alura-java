package variablesAndFlows;

public class Condicao {
    public static void main(String[] args) {
        int idade = 20;

        if (idade > 18) {
            System.out.println("Maior que 18");
        } else if(idade == 20) {
            System.out.println("Tem vinte");
        } else {
            System.out.println("Menor que 18");
        }



    }

}